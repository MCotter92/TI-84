def add(x, y):
    """
    Adds two objects together.
    """
    return x + y


def subtract(x, y):
    """
    Subtracts one object from another.
    """
    return x - y


def multiply(x, y):
    """
    Multiplies two objects together.
    """
    return x * y


def divide(x, y):
    """
    Divides x by y.
    """
    return x / y


def power(x, y):
    """
    Raises x to the power of y.
    """
    return x**y


def sqrt(x):
    """
    Finds the square root of x.
    """
    return x**0.5


